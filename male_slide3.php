<!-- 1 -->
<div class="section left">
    <h2>Overview of the Male Reproductive System</h2>
    <p>
    The male reproductive system is responsible for producing, storing,
    and delivering sperm for fertilization. It works closely with hormones,
    especially testosterone, to regulate sexual development, fertility,
    and overall reproductive health.
    </p>
    <p>
    This system also contributes to physical characteristics such as
    muscle mass, voice depth, and body hair growth. Maintaining awareness
    of how the system functions encourages responsible health choices
    and long-term well-being.
    </p>
</div>

<!-- 2 -->
<div class="section right">
    <h2>Testes – Hormone & Sperm Production</h2>
    <p>
    The testes are the primary reproductive organs in males.
    They produce sperm cells through a process called spermatogenesis
    and also generate testosterone, the key male hormone.
    </p>
    <p>
    Healthy testes are essential for fertility, energy levels,
    and sexual development. Balanced nutrition, regular exercise,
    and avoiding harmful habits help support optimal function.
    </p>
</div>

<!-- 3 -->
<div class="section left">
    <h2>Epididymis & Sperm Maturation</h2>
    <p>
    After sperm are produced in the testes, they move to the epididymis,
    a coiled structure where they mature and develop the ability to swim.
    This stage is critical for fertilization capability.
    </p>
    <p>
    Proper maturation ensures sperm are strong and functional.
    Any disruption in this process may affect fertility potential.
    </p>
</div>

<!-- 4 -->
<div class="section right">
    <h2>Vas Deferens & Sperm Transport</h2>
    <p>
    The vas deferens transports mature sperm from the epididymis
    to the urethra during ejaculation. It plays a crucial role
    in ensuring sperm reach their destination effectively.
    </p>
    <p>
    Blockages or medical conditions affecting this pathway
    can influence fertility outcomes, highlighting the importance
    of reproductive health awareness.
    </p>
</div>

<!-- 5 -->
<div class="section left">
    <h2>Prostate & Seminal Fluid Support</h2>
    <p>
    The prostate gland and seminal vesicles produce protective fluids
    that mix with sperm to form semen. These fluids nourish sperm,
    protect them from acidic environments, and enhance mobility.
    </p>
    <p>
    Maintaining hydration, balanced diet, and regular medical checkups
    helps support prostate health and reproductive efficiency.
    </p>
</div>

<!-- 6 -->
<div class="section right">
    <h2>Fertilization & Reproductive Responsibility</h2>
    <p>
    During ejaculation, millions of sperm are released.
    If one healthy sperm successfully fertilizes an egg,
    pregnancy begins. This natural process emphasizes the
    importance of sperm quality and reproductive awareness.
    </p>
    <p>
    Responsible lifestyle choices, safe practices, and emotional maturity
    are important aspects of reproductive health and partnership.
    </p>
</div>

