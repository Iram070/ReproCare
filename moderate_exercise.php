<h1>Moderate Male Fertility Exercise Plan</h1>

<!-- Card 1 -->
<div class="card">
    <h2>Resistance Training</h2>
    <ul>
        <li>Focus on moderate weight lifting 3–4 times/week.</li>
        <li>Include squats, bench press, and rows.</li>
        <li>Supports testosterone and overall strength.</li>
        <li>Perform 3 sets of 8–12 reps for each exercise.</li>
        <li>Maintain proper form to prevent injuries.</li>
    </ul>
</div>

<!-- Card 2 -->
<div class="card">
    <h2>High-Intensity Interval Training (HIIT)</h2>
    <ul>
        <li>20–25 min of alternating intense and light exercises.</li>
        <li>Includes sprints, cycling, or bodyweight circuits.</li>
        <li>Improves cardiovascular health & sperm motility.</li>
        <li>Perform 2–3 sessions/week.</li>
        <li>Rest adequately between intervals.</li>
    </ul>
</div>

<!-- Card 3 -->
<div class="card">
    <h2>Core Strengthening</h2>
    <ul>
        <li>Planks, Russian twists, and leg raises 3–4 times/week.</li>
        <li>Enhances posture and hormonal balance.</li>
        <li>Perform 3 sets of 30–60 sec per exercise.</li>
        <li>Combine with breathing exercises.</li>
        <li>Improves stability for compound movements.</li>
    </ul>
</div>

<!-- Card 4 -->
<div class="card">
    <h2>Yoga & Stretching</h2>
    <ul>
        <li>20–30 min yoga focusing on flexibility and stress relief.</li>
        <li>Reduces cortisol levels, supporting fertility.</li>
        <li>Include poses like cobra, bridge, and child’s pose.</li>
        <li>Perform 2–3 sessions/week.</li>
        <li>Deep breathing improves circulation and focus.</li>
    </ul>
</div>

<!-- Card 5 -->
<div class="card">
    <h2>Cardio Endurance</h2>
    <ul>
        <li>30–40 min moderate-intensity cardio, 3 times/week.</li>
        <li>Running, brisk walking, cycling, or swimming.</li>
        <li>Boosts cardiovascular health & hormone regulation.</li>
        <li>Maintain 60–70% of maximum heart rate.</li>
        <li>Keep sessions consistent for long-term benefits.</li>
    </ul>
</div>

<!-- Card 6 -->
<div class="card">
    <h2>Plyometric Exercises</h2>
    <ul>
        <li>Jump squats, box jumps, burpees 2–3 times/week.</li>
        <li>Enhances explosive strength & stamina.</li>
        <li>3 sets of 10–12 reps per exercise.</li>
        <li>Combine with light warm-up to avoid injury.</li>
        <li>Improves coordination and overall athleticism.</li>
    </ul>
</div>

<!-- Card 7 -->
<div class="card">
    <h2>Mobility Work</h2>
    <ul>
        <li>Dynamic stretches and joint rotations daily.</li>
        <li>Improves range of motion and reduces injury risk.</li>
        <li>Include hip, shoulder, and spine mobility exercises.</li>
        <li>5–10 minutes daily, especially before workouts.</li>
        <li>Supports better form in resistance training & HIIT.</li>
    </ul>
</div>

<!-- Card 8 -->
<div class="card">
